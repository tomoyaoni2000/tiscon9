function calculatePriceForDistance(idFrom, idTo) {
    // idFrom  idTo 
    if (idFrom > idTo) {
        [idFrom, idTo] = [idTo, idFrom];
    }

    const route = distances.find(
      d => d.id_from === idFrom && d.id_to === idTo
    );
  
    if (route) {
      return route.distance * 100;
    } else {
      return null;
    }
}

function calculatePriceForBox(ids) {
    let totalPrice = 0;

    ids.forEach(id => {
        const item = box.find(b => b.id === id);
        if (item) {
            totalPrice += item.box_count;
        }
    });

    return totalPrice;
}

